// Una función es un bloque de declaraciones que se puede utilizar repetidamente en un programa.

// Sintaxis
// func function_name() {
  // Code to be executed
// }


// Ejemplo
package main
import ("fmt")

func miFuncion() {
  fmt.Println("Hola mundo")
}

func main() {
  miFuncion()
  miFuncion()
  miFuncion()
  miFuncion()
}

// Algunas reglas al tener en cuenta
// El nombre de una función debe comenzar con una letra.
// El nombre de una función solo puede contener caracteres alfanuméricos y guiones bajos (A-z, 0-9 y _).
// Los nombres de las funciones distinguen entre mayúsculas y minúsculas.
// El nombre de una función no puede contener espacios.
// Si el nombre de la función consta de varias palabras, 
// se pueden usar técnicas introducidas para la denominación de variables con múltiples palabras.

