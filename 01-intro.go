// Un compilador, como GCC, para traducir el código Go a un lenguaje que la computadora entienda.
// En linux puedes instalarlo con el siguiente comando:
// sudo apt-get install build-essential

// Para instalar Go en Linux, puedes seguir los siguientes pasos:

// Descarga la última versión de Go para Linux desde la página oficial de descargas de Go (https://golang.org/dl/).

// Abre una terminal y navega hasta la carpeta donde descargaste el archivo de Go.

// Descomprime el archivo utilizando el siguiente comando:

// tar -C /usr/local -xzf <el-archivo-descargado>.tar.gz

// Configura la variable de entorno PATH para incluir la ubicación de 
// la carpeta bin de Go. Para hacerlo, agrega la siguiente línea al archivo .bashrc o .zshrc:

// export PATH=$PATH:/usr/local/go/bin

// Cierra y vuelve a abrir la terminal para que los cambios surtan efecto.

